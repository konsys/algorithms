/**
 * Представь, что ты — контролёр, которому нужно проверить,
 * не нарушил ли кто-то правила игры, даже если само поле заполнено лишь частично.
 * Тебе не нужно решать головоломку, нужно только убедиться, что на данный момент ошибок нет.
 * Вот пошаговый алгоритм «ручной» проверки:
 *
 * Шаг 1: Подготовь три «журнала записей»
 * Чтобы ничего не забыть, тебе нужно три списка (или таблицы):
 * Журнал строк: 9 списков (по одному для каждой горизонтали).
 * Журнал столбцов: 9 списков (по одному для каждой вертикали).
 * Журнал квадратов: 9 списков (по одному для каждого малого блока 3x3).
 *
 * Шаг 2: Начни обход поля
 * Иди по каждой клетке доски по порядку: слева направо, сверху вниз (как при чтении книги).
 *
 * Шаг 3: Проверь содержимое клетки
 * Посмотри, что написано в текущей клетке:
 * Если там точка или пусто: Просто иди к следующей клетке. Это нас не интересует.
 * Если там цифра: Переходи к следующему шагу.
 *
 * Шаг 4: Сверься с журналами
 * Допустим, ты нашел цифру «5» в 2-й строке и 4-м столбце. Она также находится во 2-м малом квадрате.
 * Загляни в журнал 2-й строки: нет ли там уже записанной пятерки?
 * Загляни в журнал 4-го столбца: нет ли там уже записанной пятерки?
 * Загляни в журнал 2-го малого квадрата: нет ли там уже записанной пятерки?
 *
 * Шаг 5: Вынеси вердикт
 * Если цифра уже где-то записана: СТОП! Правило нарушено. Доска невалидна. Дальше можно не проверять.
 * Если цифры нет ни в одном журнале: Запиши её во все три журнала (в строку, в столбец и в квадрат),
 * чтобы не пропустить её повтор в будущем.
 *
 * Шаг 6: Завершение
 * Если ты обошел все 81 клетку и ни разу не нашел повторений — поздравляю, доска валидна!
 */

export function isValidSudoku(board: string[][]): boolean {
  // Создаем массивы из 9 Set-ов для строк, столбцов и квадратов
  const rows = Array.from({ length: 9 }, () => new Set<string>());
  const cols = Array.from({ length: 9 }, () => new Set<string>());
  const boxes = Array.from({ length: 9 }, () => new Set<string>());

  for (let r = 0; r < 9; r++) {
    for (let c = 0; c < 9; c++) {
      const val = board[r][c];

      // Пропускаем пустые клетки
      if (val === '.') continue;

      // Вычисляем индекс малого квадрата (0-8)
      const boxIdx = Math.floor(r / 3) * 3 + Math.floor(c / 3);

      // Если цифра уже есть в одном из "журналов" — доска невалидна
      if (rows[r].has(val) || cols[c].has(val) || boxes[boxIdx].has(val)) {
        return false;
      }

      // Записываем цифру в журналы
      rows[r].add(val);
      cols[c].add(val);
      boxes[boxIdx].add(val);
    }
  }

  return true;
}
