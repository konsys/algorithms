import { solveSudoku } from '../solveSudoku/solveSudoku';

describe('Sudoku Solver', () => {
  // Вспомогательная функция для глубокого копирования сетки
  const copyBoard = (board: string[][]) => board.map((row) => [...row]);

  test('должен правильно решить валидное судоку (LeetCode Example 1)', () => {
    const board = [
      ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
    ];

    const expectedSolution = [
      ['5', '3', '4', '6', '7', '8', '9', '1', '2'],
      ['6', '7', '2', '1', '9', '5', '3', '4', '8'],
      ['1', '9', '8', '3', '4', '2', '5', '6', '7'],
      ['8', '5', '9', '7', '6', '1', '4', '2', '3'],
      ['4', '2', '6', '8', '5', '3', '7', '9', '1'],
      ['7', '1', '3', '9', '2', '4', '8', '5', '6'],
      ['9', '6', '1', '5', '3', '7', '2', '8', '4'],
      ['2', '8', '7', '4', '1', '9', '6', '3', '5'],
      ['3', '4', '5', '2', '8', '6', '1', '7', '9'],
    ];

    solveSudoku(board);
    expect(board).toEqual(expectedSolution);
  });

  test('решение должно удовлетворять всем правилам судоку', () => {
    const board = [
      ['.', '.', '9', '7', '4', '8', '.', '.', '.'],
      ['7', '.', '.', '.', '.', '.', '.', '.', '.'],
      ['.', '2', '.', '1', '.', '9', '.', '.', '.'],
      ['.', '.', '7', '.', '.', '.', '2', '4', '.'],
      ['.', '6', '4', '.', '1', '.', '5', '9', '.'],
      ['.', '9', '8', '.', '.', '.', '3', '.', '.'],
      ['.', '.', '.', '8', '.', '3', '.', '2', '.'],
      ['.', '.', '.', '.', '.', '.', '.', '.', '6'],
      ['.', '.', '.', '2', '7', '5', '9', '.', '.'],
    ];

    solveSudoku(board);

    // Проверка строк на уникальность 1-9
    for (let r = 0; r < 9; r++) {
      const row = board[r].filter((c) => c !== '.');
      expect(new Set(row).size).toBe(9);
    }

    // Проверка столбцов
    for (let c = 0; c < 9; c++) {
      const col = board.map((row) => row[c]);
      expect(new Set(col).size).toBe(9);
    }

    // Проверка квадратов 3x3
    for (let i = 0; i < 9; i++) {
      const box = [];
      const rowStart = Math.floor(i / 3) * 3;
      const colStart = (i % 3) * 3;
      for (let r = 0; r < 3; r++) {
        for (let c = 0; c < 3; c++) {
          box.push(board[rowStart + r][colStart + c]);
        }
      }
      expect(new Set(box).size).toBe(9);
    }
  });

  test('не должен изменять уже заполненные числа', () => {
    const initialBoard = [
      ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
    ];
    const boardCopy = copyBoard(initialBoard);

    solveSudoku(boardCopy);

    for (let r = 0; r < 9; r++) {
      for (let c = 0; c < 9; c++) {
        if (initialBoard[r][c] !== '.') {
          expect(boardCopy[r][c]).toBe(initialBoard[r][c]);
        }
      }
    }
  });
});
