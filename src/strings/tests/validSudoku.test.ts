import { isValidSudoku } from '../validSudoku/validSudoku';

describe.skip('Valid Sudoku Checker', () => {
  test('должен вернуть true для корректно заполненной (частично) доски', () => {
    const board = [
      ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
    ];
    expect(isValidSudoku(board)).toBe(true);
  });

  test('должен вернуть false, если есть дубликат в строке', () => {
    const board = [
      ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'], // В первой строке уже есть "5", добавим еще одну в ту же строку:
      ['5', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
    ];
    // Ошибка: в первом столбце две "5" (но это также и ошибка строки)
    expect(isValidSudoku(board)).toBe(false);
  });

  test('должен вернуть false, если есть дубликат в столбце', () => {
    const board = [
      ['8', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'], // "8" уже была в первом столбце выше
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
    ];
    expect(isValidSudoku(board)).toBe(false);
  });

  test('должен вернуть false, если есть дубликат в под-квадрате 3x3', () => {
    const board = [
      ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'], // В левом нижнем квадрате уже есть "6" (в 7-й строке),
      // добавим "6" в 9-ю строку того же квадрата:
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '6', '.', '.', '8', '.', '.', '7', '9'],
    ];
    expect(isValidSudoku(board)).toBe(false);
  });

  test('должен вернуть true для абсолютно пустой доски', () => {
    const emptyBoard = Array(9)
      .fill(null)
      .map(() => Array(9).fill('.'));
    expect(isValidSudoku(emptyBoard)).toBe(true);
  });
});
