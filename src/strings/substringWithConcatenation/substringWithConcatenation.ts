/**
 * @file Substring with Concatenation of All Words
 *
 * @description
 * Задача требует найти все начальные индексы подстрок в строке `s`, которые
 * являются полной конкатенацией всех слов из массива `words` в любом порядке.
 *
 * Ключевые условия:
 * - Все слова в массиве `words` имеют ОДИНАКОВУЮ длину.
 * - Каждое слово из массива должно быть использовано ровно один раз.
 * - Подстрока в `s` должна быть непрерывной, без лишних символов между словами.
 *
 * @algorithm
 * Для эффективного решения используется комбинация **Скользящего окна**
 * и **Хэш-таблиц** (частотных словарей).
 *
 * 1. ПОДГОТОВКА:
 *    - Создаем частотный словарь `wordCount` для эталонного набора слов.
 *    - Определяем длину одного слова `L` и общее количество слов `K`.
 *
 * 2. СКОЛЬЗЯЩЕЕ ОКНО (с учетом смещения):
 *    - Поскольку мы ищем блоки фиксированной длины `L`, алгоритм запускает
 *      `L` независимых проходов. Первый начинается с индекса 0, второй с 1,
 *      и так далее до `L-1`. Это гарантирует, что мы проверим все возможные
 *      разбиения строки на токены.
 *
 * 3. ОБРАБОТКА ОКНА:
 *    - В каждом проходе поддерживаем "текущее окно" слов.
 *    - Сдвигаем правую границу, извлекая слово длиной `L`.
 *    - Если слово есть в эталонном словаре:
 *        - Увеличиваем его счетчик в текущем окне.
 *        - Если счетчик слова превысил эталонный, сдвигаем левую границу окна
 *          вправо, пока количество этого слова не придет в норму.
 *        - Если количество найденных слов совпало с `K`, сохраняем индекс начала.
 *    - Если слова нет в словаре:
 *        - Полностью сбрасываем текущее окно и перемещаем левую границу за
 *          пределы текущего слова.
 *
 * @complexity
 * - Time: O(N), где N — длина строки `s`. Каждый символ посещается фиксированное
 *   количество раз в рамках своих проходов.
 * - Space: O(M), где M — суммарный размер уникальных слов в `words`.
 */
/**
 * Находит все начальные индексы подстрок, являющихся конкатенацией всех слов.
 * @param s - Исходная строка
 * @param words - Массив слов одинаковой длины
 * @returns Массив начальных индексов
 */
export function findSubstring(s: string, words: string[]): number[] {
  const result: number[] = [];
  if (!s || words.length === 0) return result;

  const wordLen = words[0].length;
  const wordCount = words.length;
  const sLen = s.length;

  // Эталонный словарь частоты слов
  const targetMap = new Map<string, number>();
  for (const word of words) {
    targetMap.set(word, (targetMap.get(word) || 0) + 1);
  }

  // Делаем wordLen проходов, чтобы покрыть все возможные смещения
  for (let i = 0; i < wordLen; i++) {
    let left = i;
    let right = i;
    let currentMap = new Map<string, number>();
    let count = 0;

    // Двигаем правый указатель с шагом wordLen
    while (right + wordLen <= sLen) {
      const word = s.substring(right, right + wordLen);
      right += wordLen;

      if (targetMap.has(word)) {
        currentMap.set(word, (currentMap.get(word) || 0) + 1);
        count++;

        // Если слов больше, чем нужно, сужаем окно слева
        while ((currentMap.get(word) || 0) > (targetMap.get(word) || 0)) {
          const leftWord = s.substring(left, left + wordLen);
          currentMap.set(leftWord, currentMap.get(leftWord)! - 1);
          count--;
          left += wordLen;
        }

        // Если количество совпало — индекс найден
        if (count === wordCount) {
          result.push(left);
        }
      } else {
        // Встретили "мусорное" слово — сбрасываем окно
        currentMap.clear();
        count = 0;
        left = right;
      }
    }
  }

  return result;
}
