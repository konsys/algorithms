/**
 * Вычисляет длину самой длинной подстроки, являющейся валидной последовательностью скобок.
 *
 * @remarks
 * Задача решается за линейное время **O(n)** с использованием стека индексов.
 * Стек инициализируется значением `-1` для корректного вычисления длины с самого начала строки.
 *
 * @param s - Строка, состоящая только из символов '(' и ')'.
 * @returns Длина самой длинной правильной скобочной подстроки.
 *
 * @example
 * ```typescript
 * // Пример 1: Частично валидная строка
 * longestValidParentheses("(()"); // Вернет 2 ("()")
 *
 * // Пример 2: Прерывистая валидность
 * longestValidParentheses(")()())"); // Вернет 4 ("()()")
 *
 * // Пример 3: Пустая строка
 * longestValidParentheses(""); // Вернет 0
 * ```
 *
 * @beta
 */

/**
 * Вычисляет длину самой длинной валидной подстроки скобок методом динамического программирования.
 *
 * @remarks
 * Алгоритм использует массив `dp`, где `dp[i]` хранит длину самой длинной правильной
 * скобочной последовательности, **заканчивающейся** в индексе `i`.
 *
 * Логика обновления для `s[i] === ')'`:
 * 1. Если `s[i-1] === '('` (вид `...()`), то `dp[i] = dp[i-2] + 2`.
 * 2. Если `s[i-1] === ')'` и символ перед найденной подстрокой `s[i - dp[i-1] - 1] === '('` (вид `...((...))`),
 *    то `dp[i] = dp[i-1] + 2 + dp[i - dp[i-1] - 2]`.
 *
 * Слой сложности: Временная **O(n)**, Пространственная **O(n)**.
 *
 * @param s - Строка из символов '(' и ')'.
 * @returns Максимальная длина найденной подстроки.
 */

/**
 *
 * Создаем массив нулей такой же длины, как наша строка:
 * s : ( ) ( ( ) )
 * dp : [0, 0, 0, 0, 0, 0]
 * 2. Идем по строке (пропускаем первый символ, он всегда 0)
 * Шаг 1: Индекс 1 (символ ))
 * Видим (). Это простая пара.
 * Логика: Берем 2 и прибавляем то, что было до этой пары (индекс -2).
 * Результат: dp[1] = 2.
 * dp: [0, 2, 0, 0, 0, 0]
 * Шаг 2: Индекс 2 (символ ()
 * Открывающая скобка сама по себе не заканчивает правильную цепочку.
 * Результат: dp[2] = 0.
 * Шаг 3: Индекс 3 (символ ()
 * Снова открывающая.
 * Результат: dp[3] = 0.
 * Шаг 4: Индекс 4 (символ ))
 * Видим (). Перед нами пара на позициях 3 и 4.
 * Логика: Прибавляем 2 к тому, что было до (индекс 2). В dp[2] у нас 0.
 * Результат: dp[4] = 2.
 * dp: [0, 2, 0, 0, 2, 0]
 * Шаг 5: Индекс 5 (символ )) — Самый важный момент!
 * Перед нами ). Но слева не (, а еще одна ). Значит, это может быть «матрешка» (вложенность).
 * Смотрим, какая длина у соседа слева: dp[4] равно 2.
 * Прыгаем через эту длину назад, чтобы найти «пару» для нашей текущей скобки. Это символ на индексе 2.
 * На индексе 2 стоит (. Ура, пара нашлась!
 * Считаем: Берем длину вложенной части (2), добавляем нашу новую пару (+2) и добавляем то,
 * что было до всей этой конструкции (смотрим dp[1], там 2).
 * Итого: 2 + 2 + 2 = 6.
 * dp: [0, 2, 0, 0, 2, 6]
 * 3. Финал
 * Максимальное число в массиве dp — это 6. Это и есть ответ.
 * Главная хитрость: Когда мы находим закрывающую скобку, мы всегда смотрим «за»
 * уже найденную готовую цепочку слева. Если там стоит открывающая скобка —
 * мы «поглощаем» её и всё, что было накоплено до неё.
 */
/**
 * Находит длину самой длинной правильной последовательности скобок.
 * Использует динамическое программирование (DP).
 *
 * Сложность по времени: O(n) — один проход по строке.
 * Сложность по памяти: O(n) — массив для хранения длин.
 */
export function longestValidParentheses(s: string): number {
  const n = s.length;
  if (n === 0) return 0;

  // dp[i] хранит длину самой длинной валидной подстроки, заканчивающейся в i
  const dp: number[] = new Array(n).fill(0);
  let maxLen = 0;

  for (let i = 1; i < n; i++) {
    // Нас интересуют только закрывающие скобки, т.к. они завершают последовательность
    if (s[i] === ')') {
      // СЛУЧАЙ 1: Простая пара прямо перед нами "()", например: "()()"
      if (s[i - 1] === '(') {
        // Берем 2 и прибавляем результат, который был ДО этой пары (dp[i-2])
        dp[i] = (i >= 2 ? dp[i - 2] : 0) + 2;
      }

      // СЛУЧАЙ 2: Вложенная структура "(())" или сложная "()(())"
      // Если перед текущей ')' стоит другая ')', ищем парную '(' для текущей
      else {
        // Вычисляем индекс, где должна быть открывающая скобка для текущей s[i]
        const openIdx = i - dp[i - 1] - 1;

        if (openIdx >= 0 && s[openIdx] === '(') {
          // 1. Берем длину внутренней части: dp[i-1]
          // 2. Добавляем 2 (нашу новую внешнюю пару)
          // 3. Добавляем то, что было ДО всей этой конструкции: dp[openIdx - 1]
          const beforeMatch = openIdx >= 1 ? dp[openIdx - 1] : 0;
          dp[i] = dp[i - 1] + 2 + beforeMatch;
        }
      }

      // Обновляем глобальный максимум
      maxLen = Math.max(maxLen, dp[i]);
    }
  }

  return maxLen;
}
