/**
 * @file Generate Parentheses
 * @description
 * Учитывая n пар круглых скобок, необходимо сгенерировать все комбинации
 * правильных (хорошо сформированных) скобочных последовательностей.
 *
 * Правильная последовательность означает, что каждая открывающая скобка
 * имеет соответствующую закрывающую, и они расположены в корректном порядке.
 *
 * @param {number} n - Количество пар круглых скобок. 1 <= n <= 8.
 * @returns {string[]} Список всех валидных комбинаций скобок.
 *
 * @example
 * // n = 3
 * // Input: n = 3
 * // Output: ["((()))","(()())","(())()","()(())","()()()"]
 *
 * @example
 * // n = 1
 * // Input: n = 1
 * // Output: ["()"]
 */

/**
 * АЛГОРИТМ (Концептуальное описание):
 *
 * 1. ИСПОЛЬЗУЕМЫЙ МЕТОД:
 *    Рекурсивный поиск с возвратом (Backtracking). Мы строим строку посимвольно,
 *    принимая решение на каждом шагу, какую скобку добавить.
 *
 * 2. ПРАВИЛА ВЕТВЛЕНИЯ:
 *    - Добавление '(': Мы можем добавить открывающую скобку в любой момент,
 *      пока их общее количество меньше n.
 *    - Добавление ')': Мы можем добавить закрывающую скобку только в том случае,
 *      если количество уже поставленных закрывающих скобок меньше количества открывающих.
 *
 * 3. УСЛОВИЕ ЗАВЕРШЕНИЯ:
 *    - Когда длина текущей строки достигает n * 2, мы получаем одну из валидных
 *      комбинаций и сохраняем её в итоговый список.
 *
 * 4. ПРЕИМУЩЕСТВО ПОДХОДА:
 *    Этот метод гарантирует, что мы генерируем только правильные последовательности,
 *    полностью избегая создания и последующей проверки некорректных вариантов.
 */

export function generateParenthesis(n: number) {
  /**
   * Генерирует все комбинации правильных скобочных последовательностей.
   *
   * @step 1 ИНИЦИАЛИЗАЦИЯ (Начало пути)
   * Мы создаем пустой список для хранения результатов и запускаем процесс
   * построения строки с нуля. У нас есть два счетчика: `open` (сколько '(' мы поставили)
   * и `close` (сколько ')' мы поставили).
   **/
  const list: string[] = [];
  let resultStringAr: string[] = [];

  function backtrack(open: number, close: number) {
    /** @step 4 БАЗОВЫЙ СЛУЧАЙ (Финиш)
     * Процесс останавливается, когда длина нашей строки становится равна `n * 2`.
     * Это означает, что все пары скобок расставлены. Мы сохраняем эту строку.
     **/
    if (resultStringAr.length === n * 2) {
      list.push(resultStringAr.join(''));
    }

    /** @step 2 ПРАВИЛО ЛЕВОЙ СКОБКИ (Открытие)
     * Мы всегда можем добавить открывающую скобку '(', если их количество
     * в текущей строке еще не достигло максимума `n`.
     * Условие: `if (open < n)` -> Добавить '(' -> Увеличить `open` на 1.
     **/
    if (open < n) {
      resultStringAr.push('(');
      backtrack(open + 1, close);
      resultStringAr.pop();
    }
    /** @step 3 ПРАВИЛО ПРАВОЙ СКОБКИ (Закрытие)
     * Мы можем добавить закрывающую скобку ')' только в том случае, если
     * прямо сейчас в строке есть "незакрытые" левые скобки.
     * Условие: `if (close < open)` -> Добавить ')' -> Увеличить `close` на 1.
     **/
    if (close < open) {
      resultStringAr.push(')');
      backtrack(open, close + 1);
      resultStringAr.pop();
    }
    /** @step 5 ПОИСК С ВОЗВРАТОМ (Backtracking)
     * После того как мы дошли до финиша по одной "ветке" выбора, мы возвращаемся
     * на шаг назад, чтобы попробовать другой вариант (например, вместо '(' поставить ')'),
     * если это позволяют правила из Шага 2 и 3.
     **/
  }

  backtrack(0, 0);
  return list;
}

/**
 * Генерирует все комбинации скобок методом динамического программирования.
 *
 * @remarks
 * Формула: f(n) = ( f(i) ) + f(n - 1 - i), где i от 0 до n-1.
 * Мы сохраняем результаты для маленьких n, чтобы собрать из них большие.
 *
 * @param n - Количество пар скобок.
 * @returns Массив всех валидных комбинаций.
 */
export function generateParenthesisDyn(n: number): string[] {
  // dp[i] будет хранить список всех правильных комбинаций для i пар скобок
  const dp: string[][] = Array.from({ length: n + 1 }, () => []);

  // Базовый случай: для 0 пар скобок есть только одна пустая строка
  dp[0] = [''];

  // Идем от 1 пары до n
  for (let currentN = 1; currentN <= n; currentN++) {
    // Применяем формулу: ( A ) B
    // i - количество пар внутри скобок (A)
    // j - количество пар снаружи скобок (B)
    for (let i = 0; i < currentN; i++) {
      const j = currentN - 1 - i;

      // Берем каждую возможную комбинацию A и B
      for (const strA of dp[i]) {
        for (const strB of dp[j]) {
          // Собираем новую строку по шаблону (A)B
          dp[currentN].push(`(${strA})${strB}`);
        }
      }
    }
  }

  return dp[n];
}
