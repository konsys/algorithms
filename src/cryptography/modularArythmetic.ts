/*
1. Основное понятие: Операция Modulo

Результат операции \(a\quad (\mod n)\) — это остаток от деления целого числа \(a\) на натуральное число \(n\) (модуль)

2. Сравнение по модулю (Конгруэнтность)
Два числа \(a\) и \(b\) называются сравнимыми по модулю \(n\), если они дают одинаковый остаток при делении на \(n\)

3. Основные операции и их свойства
Главное удобство для криптографии в том, что остаток можно брать на любом этапе вычислений

Сложение: \((a+b)\quad (\mod n)=[(a\quad (\mod n))+(b\quad (\mod n))]\quad (\mod n)\).
Умножение: \((a\times b)\quad (\mod n)=[(a\quad (\mod n))\times (b\quad (\mod n))]\quad (\mod n)\).
Возведение в степень: \(a^{b}\quad (\mod n)\) (критично для RSA и Диффи-Хеллмана).
Благодаря свойствам умножения, можно возводить в огромные степени, не вычисляя само гигантское число


4. Особые операции в криптографии
Обратный элемент по модулю: В модулярной арифметике нет обычного деления.
Вместо него ищут число \(x\), такое что \((a\times x)\equiv 1\quad (\mod n)\).
Это используется для расшифровки сообщений

Дискретное логарифмирование: Обратная операция к возведению в степень.
В обычной математике это легко, а в модулярной (при больших \(n\)) — практически невозможно,
что и обеспечивает защиту алгоритмов


Почему это важно?
Ограничение памяти: Числа никогда не становятся бесконечно большими, они всегда остаются в диапазоне \([0,n-1]\).
Необратимость: Глядя на результат \(x\quad (\mod n)=5\), невозможно точно сказать, каким было исходное \(x\) (\(5,17,29...\)),
что создает почву для криптографических ловушек

 */