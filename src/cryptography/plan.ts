// Криптография — это дисциплина, объединяющая математику, информатику и практическую безопасность. Ниже представлен структурированный план обучения от основ до продвинутых тем.
// 1. Фундамент и Математический базис
// Прежде чем изучать алгоритмы, необходимо освоить инструменты, на которых они строятся:
//   Теория чисел: Модулярная арифметика, алгоритм Евклида, малая теорема Ферма и теорема Эйлера.
//   Теория вероятностей: Основы случайности и распределения данных.
//   Сложность вычислений: Понятие «односторонних функций» (one-way functions) и трудоемкость взлома.
// 2. Классическая криптография (История)
// Изучение простых шифров помогает понять базовые принципы:
//   Шифры замены и перестановки: Шифр Цезаря, Виженера, Скитала.
//   Слабости: Частотный анализ и методы взлома простых систем.
// 3. Симметричное шифрование
// Один ключ для зашифровки и расшифровки:
//   Поточные шифры: Принцип One-Time Pad (одноразовый блокнот) и его абсолютная стойкость.
//   Блочные шифры: Архитектура (сеть Фейстеля, SP-сеть), алгоритмы AES, DES, ГОСТ.
//   Режимы работы: ECB, CBC, GCM (как безопасно шифровать большие данные).
// 4. Асимметричная криптография (Public Key)
// Использование пары «открытый/закрытый ключ»:
// Алгоритм RSA: Основан на сложности факторизации больших чисел.
//   Протокол Диффи-Хеллмана: Метод безопасного обмена ключами по открытому каналу.
//   Эллиптические кривые (ECC): Современный стандарт (более короткие ключи при высокой стойкости).
// 5. Целостность данных и Аутентификация
// Как убедиться, что данные не изменили по пути:
//   Хеш-функции: SHA-256, SHA-3; свойства (отсутствие коллизий, необратимость).
// Коды аутентификации (MAC): Подтверждение подлинности сообщения.
//   Цифровая подпись: Электронное подтверждение авторства.
// 6. Протоколы и Инфраструктура
// Практическое применение в реальном мире:
//   PKI (Инфраструктура открытых ключей): Сертификаты X.509, центры сертификации (CA).
//   SSL/TLS: Основа безопасности интернета (HTTPS).
// 7. Современные и продвинутые темы
// Направления, определяющие будущее отрасли в 2025 году:
//   Постквантовая криптография (PQC): Алгоритмы, устойчивые к взлому квантовыми компьютерами (стандарты NIST: ML-KEM, ML-DSA).
// Доказательства с нулевым разглашением (ZKP): Подтверждение владения секретом без раскрытия самого секрета.
//   Гомоморфное шифрование: Вычисления над зашифрованными данными

