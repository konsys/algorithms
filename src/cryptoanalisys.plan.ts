/*
Криптоанализ — это искусство поиска уязвимостей в шифре без знания ключа. Все методы можно разделить на классические (математические) и физические (атаки по сторонним каналам).

1. Математические атаки (Логические)
Эти методы ищут «дыры» в самой архитектуре (например, в сети Фейстеля или SP-сети).
Линейный криптоанализ: Поиск статистических связей (линейных уравнений) между открытым текстом, ключом и шифртекстом.
 Если уравнение верно чуть чаще, чем в 50% случаев, ключ можно вычислить.
  Дифференциальный криптоанализ: Изучение того, как разница на входе (изменение 1 бита) влияет на разницу на выходе.
  Помогает понять структуру S-блоков и найти ключи раундов.
  Интегральный криптоанализ: Анализ поведения целых групп блоков данных (а не отдельных бит). Особенно эффективен против AES на малом количестве раундов.
  Атака «Birthday Attack» (День рождения): Поиск коллизий (когда разные данные дают одинаковый шифртекст). Основана на теории вероятностей: в 64-битных шифрах шансы на совпадение блоков растут очень быстро.
2. Атаки по сценариям (Доступ к данным)
Классификация зависит от того, что именно «украл» хакер:
  COA (Ciphertext-Only): У хакера есть только зашифрованные данные. Самый сложный случай (нужен частотный анализ).
KPA (Known-Plaintext): Хакер знает и текст, и его зашифрованную версию (например, перехватил стандартный заголовок файла).
CPA (Chosen-Plaintext): Хакер может сам зашифровать любой текст своим ключом и посмотреть результат (часто доступно в веб-сервисах).
CCA (Chosen-Ciphertext): Хакер может подсунуть свой шифртекст на расшифровку и увидеть, что получится.
3. Атаки по сторонним каналам (Side-Channel)
Самые коварные атаки. Они не взламывают математику, они «подсматривают» за компьютером:
  Timing Attack: Измерение времени, которое процессор тратит на шифрование (разные биты ключа обрабатываются с разной скоростью).
Power Analysis: Замер потребления электроэнергии чипом во время работы с ключом.
  Acoustic/EM Attack: Анализ звуков или электромагнитного излучения от процессора.
4. Атаки «грубой силы» (Brute Force)
Полный перебор: Попытка угадать ключ, пробуя все возможные комбинации. Единственная защита — увеличение длины ключа (например, переход с 128 бит на 256 бит).
Rainbow Tables: Использование огромных заранее вычисленных таблиц с результатами шифрования популярных паролей.
  Что из этого списка кажется вам наиболее опасным для современных систем? Bolding на типах атак поможет быстро пробежать глазами.

  */

