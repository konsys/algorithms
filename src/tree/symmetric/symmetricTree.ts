// Определение узла бинарного дерева (обычно дается в LeetCode)
export class TreeNode {
    val: number;
    left: TreeNode | null;
    right: TreeNode | null;
    constructor(val?: number, left?: TreeNode | null, right?: TreeNode | null) {
        this.val = (val === undefined ? 0 : val);
        this.left = (left === undefined ? null : left);
        this.right = (right === undefined ? null : right);
    }
}
   
export function isSymmetric(root: TreeNode | null): boolean {
    // Если дерево пустое, оно симметрично
    if (!root) return true;

    // Запускаем проверку двух веток как отдельных деревьев
    return isMirror(root.left, root.right);
}

  function isMirror(t1: TreeNode | null, t2: TreeNode | null): boolean {   
    // Базовый случай 1: оба узла пустые — это симметрия
    if (t1 === null && t2 === null) return true;

    // Базовый случай 2: один пустой, а другой нет — не симметрия
    if (t1 === null || t2 === null) return false;

    // Базовый случай 3: значения в узлах не совпадают — не симметрия
    if (t1.val !== t2.val) return false;

    // Рекурсия:
    // Сравниваем лево первого с право второго И право первого с лево второго
    return isMirror(t1.left, t2.right) && isMirror(t1.right, t2.left);
}