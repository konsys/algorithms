/**
 * Перестраивает массив чисел в следующую лексикографически большую перестановку.
 *
 * Если следующая перестановка невозможна (массив отсортирован по убыванию),
 * перестраивает его в наименьший возможный порядок (сортирует по возрастанию).
 *
 * @param {number[]} nums - Массив целых чисел, который нужно изменить.
 * @return {void} Не возвращает значение, модифицирует массив nums на месте (in-place).
 *
 * @example
 * // Ввод: [1, 2, 3] -> Результат: [1, 3, 2]
 * // Ввод: [3, 2, 1] -> Результат: [1, 2, 3]
 * // Ввод: [1, 1, 5] -> Результат: [1, 5, 1]
 *
 * @description
 * Алгоритм (O(n) время, O(1) память):
 * 1. Найти первый элемент nums[i] с конца, такой что nums[i] < nums[i + 1].
 * 2. Если индекс найден, найти nums[j] с конца, такой что nums[j] > nums[i], и поменять их местами.
 * 3. Развернуть (reverse) часть массива справа от индекса i.
 */

/**
 * @function nextPermutation
 * @description Реализует алгоритм поиска следующей лексикографической перестановки.
 *
 * 1. ПОИСК ТОЧКИ ПЕРЕГИБА (Scan from right):
 *    Двигаться с конца массива, пока не найдется первый элемент nums[i],
 *    который МЕНЬШЕ своего соседа справа (nums[i] < nums[i + 1]).
 *    Этот элемент — самый правый "разряд", который можно увеличить.
 * 2. ОБРАБОТКА МАКСИМАЛЬНОГО ВАРИАНТА (Edge case):
 *    Если такая точка не найдена (i < 0), значит массив отсортирован по убыванию.
 *    В этом случае нужно просто РАЗВЕРНУТЬ весь массив, чтобы получить минимальный порядок.
 *
 * 3. ПОИСК ЗАМЕНЫ (Find successor):
 *    Если точка i найдена, снова пройти с конца массива и найти первый
 *    элемент nums[j], который БОЛЬШЕ nums[i].
 *
 * 4. ОБМЕН (Swap):
 *    Поменять местами элементы nums[i] и nums[j]. Теперь в позиции i стоит
 *    минимально возможное число, которое больше предыдущего.
 *
 * 5. ВЫРАВНИВАНИЕ ХВОСТА (Reverse suffix):
 *    Все элементы справа от индекса i теперь гарантированно идут по убыванию.
 *    Чтобы получить наименьшее приращение, нужно РАЗВЕРНУТЬ (reverse) эту
 *    часть массива, чтобы она шла по возрастанию.
 * 1 3 1 2 -> 3 1 1 2
 * @param {number[]} nums - Исходный массив для модификации на месте.
 * @returns {void} Модифицирует массив in-place.
 */
export function nextPermutation(nums: number[]): void {
  for (let i = nums.length - 1; i >= 0; i--) {
    if (i === 0) {
      nums.reverse();
      return;
    }
    const pivot = i - 1;
    if (nums[i - 1] < nums[i]) {
      for (let j = nums.length - 1; j >= 0; j--) {
        if (nums[j] > nums[pivot]) {
          const temp = nums[j];
          nums[j] = nums[pivot];
          nums[pivot] = temp;

          let start = i;
          let end = nums.length - 1;

          while (start < end) {
            [nums[start], nums[end]] = [nums[end], nums[start]];

            start++;
            end--;
          }
          return;
        }
      }
    }
  }
}
