/**
 * Удаляет дубликаты из отсортированного массива на месте (in-place).
 *
 * @description
 * Функция принимает отсортированный массив и изменяет его так, чтобы каждый
 * уникальный элемент встречался только один раз. Относительный порядок элементов сохраняется.
 *
 * @algorithm
 * 1. Используем два указателя: `i` (текущий элемент) и `k` (позиция для записи уникального).
 * 2. Первый элемент всегда уникален, поэтому начинаем с `i = 1`.
 * 3. Если `nums[i]` отличается от `nums[i - 1]`, записываем его в `nums[k]` и увеличиваем `k`.
 * 4. Элементы массива после индекса `k-1` игнорируются.
 *
 * @param {number[]} nums - Отсортированный по возрастанию массив чисел.
 * @returns {number} Количество уникальных элементов k.
 *
 * @complexity
 * - **Time Complexity**: O(n) — один проход по массиву длиной n.
 * - **Space Complexity**: O(1) — дополнительная память не используется.
 *
 * @example
 * const nums1 = [1, 1, 2];
 * const k1 = removeDuplicates(nums1);
 * // k1 = 2, nums1 станет [1, 2, 1] (первые 2 элемента валидны)
 *
 * @example
 * const nums2 = [0, 0, 1, 1, 1, 2, 2, 3, 3, 4];
 * const k2 = removeDuplicates(nums2);
 * // k2 = 5, nums2 станет [0, 1, 2, 3, 4, 2, 2, 3, 3, 4]
 */
export function removeDuplicates(nums: number[]): number {
  const n = nums.length;
  if (n === 0) return 0;

  let k: number = 1; // Указатель для вставки уникального элемента

  for (let i = 1; i < n; i++) {
    // Если нашли новый уникальный элемент
    if (nums[i] !== nums[i - 1]) {
      nums[k] = nums[i];
      k++;
    }
  }

  return k;
}
