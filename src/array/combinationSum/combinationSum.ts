/**
 * Представь, что ты стоишь перед прилавком с конфетами разных видов, и у тебя в кармане ровно 7 рублей.
 * Тебе нужно найти все способы потратить их полностью.
 * Вот как работает этот алгоритм (Backtracking) по шагам:
 * 1. Подготовка
 * Сначала мы раскладываем конфеты (числа) по возрастанию цены. Это нужно, чтобы не проверять лишнее:
 * если конфета за 5 рублей тебе уже не по карману, то конфету за 10 рублей рассматривать и вовсе нет смысла.
 **/

/** 2. Принцип «Дерева решений»
 * Мы начинаем строить маршруты. Каждый выбор — это ветка дерева.
 * Шаг «Вперед»: Берем первую доступную конфету и вычитаем её цену из нашего бюджета.
 **/

/** Выбор сделан: Теперь у нас новый остаток денег. Мы снова можем выбрать эту же самую конфету или любую следующую за ней.
 * Тупик (Превышение): Если мы набрали конфет на сумму больше 7 рублей, мы останавливаемся. Этот путь ложный.
 * Успех (Цель): Если после очередной покупки у нас осталось ровно 0 рублей — поздравляю,
 * этот набор конфет мы записываем в блокнот как идеальный вариант.
 * 3. Шаг назад (Backtracking)
 * Это самая важная часть. Когда мы дошли до тупика или нашли решение:
 * Мы «откладываем» последнюю купленную конфету обратно на прилавок.
 * Возвращаем её стоимость в наш бюджет.
 * Пробуем вместо неё взять следующую по списку конфету.
 * 4. Защита от повторов
 * Чтобы не записывать одинаковые наборы в разном порядке (например, сначала за 2 рубля,
 * потом за 3, или наоборот), у нас есть строгое правило:
 * Если ты сейчас рассматриваешь конфету №2, ты больше не имеешь права смотреть на конфету №1.
 * Только на №2 и те, что стоят дальше.
 * Пример на пальцах (Цель 7, числа [2, 3, 6, 7])
 * Берем 2. Осталось 5.
 * Снова берем 2. Осталось 3.
 * Снова берем 2. Осталось 1. (Следующие числа больше 1, тупик).
 * Шаг назад: Убираем последнюю 2. Теперь у нас снова 3 рубля.
 * Берем следующее число — 3. Осталось 0. Ура! Записали: [2, 2, 3].
 * Шаг назад: Убираем 3, пробуем 6... слишком много.
 * Шаг назад: Убираем вторую 2... и так далее, пока не переберем все стартовые варианты.
 */

/**
 * Решение задачи Combination Sum (Medium)
 *
 * Сложность по времени: O(N^(T/M + 1)), где N — кол-во кандидатов,
 * T — target, M — минимальное значение в candidates.
 * Сложность по памяти: O(T/M) для стека рекурсии.
 */
export function combinationSum(candidates: number[], target: number): number[][] {
  const result: number[][] = [];

  // 1. Сортируем массив для оптимизации (позволит сделать break в цикле)
  candidates.sort((a, b) => a - b);

  /**
   * @param remain Текущий остаток до цели
   * @param start Индекс, с которого начинаем перебор (чтобы не было дубликатов)
   * @param path Текущая собираемая комбинация
   */
  function backtrack(remain: number, start: number, path: number[]): void {
    // Базовый случай: цель достигнута
    if (remain === 0) {
      result.push([...path]); // Копируем массив, так как path меняется дальше
      return;
    }

    for (let i = start; i < candidates.length; i++) {
      const num = candidates[i];

      // Оптимизация: если текущее число больше остатка,
      // то все последующие числа тем более будут больше (т.к. массив отсортирован)
      if (num > remain) {
        break;
      }

      // Делаем выбор
      path.push(num);

      // Рекурсивный вызов:
      // Передаем i, а не i + 1, потому что можем использовать то же число повторно
      backtrack(remain - num, i, path);

      // Отмена выбора (backtrack)
      path.pop();
    }
  }

  backtrack(target, 0, []);
  return result;
}
