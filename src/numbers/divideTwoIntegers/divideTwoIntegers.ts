/**
 * @file Решение задачи "Divide Two Integers" без использования *, / и %.
 */

/**
 * Делит два целых числа, используя только битовые операции и вычитание.
 *
 * @param {number} dividend - Число, которое делим (Делимое).
 * @param {number} divisor - Число, на которое делим (Делитель).
 * @returns {number} Частное, округленное до целого в сторону нуля.
 *
 * @example
 * // Алгоритм "Удваивание вычитаемого":
 * // 1. Определяем знак результата (разные знаки -> минус).
 * // 2. Работаем с абсолютными значениями (Math.abs).
 * // 3. Пока делимое >= делителя:
 * //    - Находим максимально возможное число (делитель * 2^n), которое можно вычесть.
 * //    - Вычитаем его, прибавляем 2^n к результату.
 * // 4. Проверяем границы 32-битного целого числа.
 */

/**
 * Деление двух целых чисел без использования *, / и %
 * Сложность: O(log^2 N)
 */
export function divide(dividend: number, divisor: number): number {
  // 1. Константы для 32-битного диапазона
  const MAX_INT = 2147483647; // 2^31 - 1
  const MIN_INT = -2147483648; // -2^31

  // 2. Обработка переполнения (единственный случай: -2^31 / -1)
  if (dividend === MIN_INT && divisor === -1) {
    return MAX_INT;
  }

  // 3. Определяем знак результата (true, если знаки разные)
  const isNegative = dividend < 0 !== divisor < 0;

  // 4. Работаем с положительными значениями
  let a = Math.abs(dividend);
  let b = Math.abs(divisor);
  let quotient = 0;

  // 5. Основной цикл "пакетирования"
  while (a >= b) {
    let tempDivisor = b;
    let multiple = 1;

    // "Удваиваем" делитель, пока он помещается в остаток
    // Используем арифметику, так как битовый сдвиг в JS ограничен 32 битами
    while (a >= tempDivisor + tempDivisor) {
      tempDivisor += tempDivisor; // Аналог tempDivisor << 1
      multiple += multiple; // Аналог multiple << 1
    }

    // Вычитаем самую большую найденную "пачку"
    a -= tempDivisor;
    // Добавляем количество вхождений в итоговый ответ
    quotient += multiple;
  }

  // 6. Возвращаем результат с учетом знака
  return 0 + (isNegative ? -quotient : quotient);
}
