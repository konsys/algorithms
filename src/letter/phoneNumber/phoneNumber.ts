/*
 * =========================================================================
 * ЗАДАЧА: Буквенные комбинации номера телефона
 * =========================================================================
 *
 * ПРЕДИСТОРИЯ:
 * Представь старый кнопочный телефон. На кнопке '2' написаны буквы 'abc',
 * на кнопке '3' — 'def' и так далее. Нам дают строку из цифр (например, "23"),
 * и нам нужно найти ВСЕ возможные текстовые комбинации, которые можно нажать.
 *
 * ПРИМЕР:
 * Ввод: "23"
 * Результат: ["ad", "ae", "af", "bd", "be", "bf", "cd", "ce", "cf"]
 *
 * ОГРАНИЧЕНИЯ:
 * - Длина строки от 0 до 4 символов.
 * - Используются только цифры от 2 до 9.
 */

/*
 * =========================================================================
 * АЛГОРИТМ: Пошаговый план решения (Backtracking)
 * =========================================================================
 *
 * ШАГ 1: ПОДГОТОВКА СПРАВОЧНИКА
 * Создаем объект-карту (Map), где прописываем соответствие:
 * '2' -> 'abc', '3' -> 'def', ..., '9' -> 'wxyz'.
 * Это наша база данных для поиска.
 *
 * ШАГ 2: ОБРАБОТКА ПУСТОГО ВВОДА
 * Если на вход пришла пустая строка "", мы сразу возвращаем пустой массив [].
 * Нельзя составить слова из ничего.
 *
 * ШАГ 3: СОЗДАНИЕ ГЛАВНОГО ХРАНИЛИЩА
 * Объявляем пустой массив (например, 'result'), куда мы будем складывать
 * каждое найденное готовое "слово".
 *
 * ШАГ 4: ЗАПУСК ГЕНЕРАТОРА (РЕКУРСИВНАЯ ФУНКЦИЯ)
 * Создаем функцию, которая будет "путешествовать" по цифрам.
 * Ей нужно передавать:
 *   - Индекс цифры, которую мы обрабатываем сейчас (0, 1, 2...).
 *   - Текущую строку, которую мы успели собрать к этому моменту.
 *
 * ШАГ 5: УСЛОВИЕ ОСТАНОВКИ (БАЗОВЫЙ СЛУЧАЙ)
 * Внутри генератора проверяем: "Если длина собранной строки равна
 * количеству цифр на входе, значит слово готово!"
 *   - Сохраняем это слово в 'result'.
 *   - Выходим из этой ветки (return), чтобы искать другие варианты.
 *
 * ШАГ 6: ПЕРЕБОР ВАРИАНТОВ ДЛЯ ТЕКУЩЕЙ ЦИФРЫ
 * Если слово еще не готово:
 *   - Берем текущую цифру по индексу.
 *   - Находим в справочнике (ШАГ 1) буквы для этой цифры.
 *   - Запускаем цикл по этим буквам.
 *
 * ШАГ 7: ПОГРУЖЕНИЕ (РЕКУРСИЯ)
 * Для каждой буквы из цикла:
 *   - Вызываем генератор СНОВА для СЛЕДУЮЩЕГО индекса (индекс + 1).
 *   - К текущей строке приклеиваем выбранную букву.
 *
 * ШАГ 8: ФИНАЛ
 * Когда генератор закончит работу во всех ветках, массив 'result'
 * будет заполнен всеми возможными комбинациями. Возвращаем его.
 */
export function letterCombinations(s: string): string[] {
  /*
   * ШАГ 1: ПОДГОТОВКА СПРАВОЧНИКА
   * Создаем объект-карту (Map), где прописываем соответствие:
   * '2' -> 'abc', '3' -> 'def', ..., '9' -> 'wxyz'.
   * Это наша база данных для поиска.
   */
  const input = [
    {
      '2': 'abc',
    },
    {
      '3': 'def',
    },
    {
      '4': 'ghi',
    },
    {
      '5': 'jkl',
    },
    {
      '6': 'mno',
    },
    {
      '7': 'pqrs',
    },
    {
      '8': 'tuv',
    },
    {
      '9': 'wxyz',
    },
  ];
  const map = new Map<string, string>(input.flatMap((obj) => Object.entries(obj)));
  /*
   * ШАГ 2: ОБРАБОТКА ПУСТОГО ВВОДА
   * Если на вход пришла пустая строка "", мы сразу возвращаем пустой массив [].
   * Нельзя составить слова из ничего.
   */
  if (!s) {
    return [];
  }
  /*
   * ШАГ 3: СОЗДАНИЕ ГЛАВНОГО ХРАНИЛИЩА
   * Объявляем пустой массив (например, 'result'), куда мы будем складывать
   * каждое найденное готовое "слово".
   */
  const result: string[] = [];

  /*
   * ШАГ 4: ЗАПУСК ГЕНЕРАТОРА (РЕКУРСИВНАЯ ФУНКЦИЯ)
   * Создаем функцию, которая будет "путешествовать" по цифрам.
   * Ей нужно передавать:
   *   - Индекс цифры, которую мы обрабатываем сейчас (0, 1, 2...).
   *   - Текущую строку, которую мы успели собрать к этому моменту.
   */
  function generateCombinations(index: number, currentString: string) {
    /*
     * ШАГ 5: УСЛОВИЕ ОСТАНОВКИ (БАЗОВЫЙ СЛУЧАЙ)
     * Внутри генератора проверяем: "Если длина собранной строки равна
     * количеству цифр на входе, значит слово готово!"
     *   - Сохраняем это слово в 'result'.
     *   - Выходим из этой ветки (return), чтобы искать другие варианты.
     */
    // wrong
    // if (currentString.length === index) {
    if (index === s.length) {
      result.push(currentString);
      return;
    }
    /*
     * ШАГ 6: ПЕРЕБОР ВАРИАНТОВ ДЛЯ ТЕКУЩЕЙ ЦИФРЫ
     * Если слово еще не готово:
     *   - Берем текущую цифру по индексу.
     *   - Находим в справочнике (ШАГ 1) буквы для этой цифры.
     *   - Запускаем цикл по этим буквам.
     */
    // wrong
    // const currentVariants = map.get(index.toString()) ?? '';
    const currentVariants = map.get(s[index]) ?? '';

    /*
     * ШАГ 7: ПОГРУЖЕНИЕ (РЕКУРСИЯ)
     * Для каждой буквы из цикла:
     *   - Вызываем генератор СНОВА для СЛЕДУЮЩЕГО индекса (индекс + 1).
     *   - К текущей строке приклеиваем выбранную букву.
     */
    // wrong
    // generateCombinations(index + 1, currentVariants);

    // ШАГ 7: Обязательный ЦИКЛ по буквам
    for (const char of currentVariants) {
      // Погружаемся дальше, увеличивая индекс и добавляя букву
      generateCombinations(index + 1, currentString + char);
    }
  }

  generateCombinations(0, '');
  return result;
}
