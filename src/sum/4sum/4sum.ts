/**
 * ЗАДАЧА: 4Sum (Четыре слагаемых)
 *
 * ОПИСАНИЕ:
 * Дано: Массив целых чисел 'nums' и число 'target'.
 * Найти: Все уникальные четверки чисел [nums[a], nums[b], nums[c], nums[d]],
 * сумма которых в точности равна 'target'.
 *
 * УСЛОВИЯ:
 * 1. Все индексы (a, b, c, d) должны быть разными.
 * 2. В итоговом списке не должно быть дубликатов (одинаковых четверок чисел).
 * 3. Порядок четверок в ответе не важен.
 *
 * ПРИМЕР:
 * Ввод: nums = [1, 0, -1, 0, -2, 2], target = 0
 * Вывод: [[-2, -1, 1, 2], [-2, 0, 0, 2], [-1, 0, 0, 1]]
 */
export function fourSum(nums: number[], target: number): number[][] {
  /**
   * АЛГОРИТМ РЕШЕНИЯ (Step-by-Step):
   *
   * 1. ПОДГОТОВКА (СОРТИРОВКА):
   *    - Сортируем массив `nums` по возрастанию.
   *    - Это позволит нам:
   *      а) Легко пропускать повторяющиеся числа.
   *      б) Эффективно сужать область поиска с помощью двух указателей.
   */
  const result: number[][] = [];
  nums.sort((a, b) => a - b);
  /**
   * 2. ПЕРВЫЙ УРОВЕНЬ (Фиксация первого числа 'i'):
   *    - Цикл от 0 до n-4.
   *    - ПРОПУСК ДУБЛИКАТОВ: Если nums[i] == nums[i-1], идем дальше.
   *    - ОПТИМИЗАЦИЯ: Если сумма nums[i] и трех минимальных чисел > target,
   *      прерываем цикл (дальше будет только больше).
   */
  for (let i = 0; i < nums.length - 3; i++) {
    if (i > 0 && nums[i] === nums[i - 1]) {
      continue;
    }
    /**
     * 3. ВТОРОЙ УРОВЕНЬ (Фиксация второго числа 'j'):
     *    - Вложенный цикл от i+1 до n-3.
     *    - ПРОПУСК ДУБЛИКАТОВ: Если nums[j] == nums[j-1] (внутри диапазона), идем дальше.
     *    - Теперь у нас есть "база" из двух чисел, ищем оставшиеся два.
     */
    for (let j = i + 1; j < nums.length - 2; j++) {
      if (j > i + 1 && nums[j] === nums[j - 1]) {
        continue;
      }
      /**
       * 4. ТРЕТИЙ УРОВЕНЬ (Метод двух указателей):
       *    - Ставим указатель 'left' на j + 1.
       *    - Ставим указатель 'right' на самый конец массива.
       *
       *    - ПОКА left < right:
       *      1. Считаем текущую сумму четырех чисел.
       *
       *      2. ЕСЛИ сумма == target:
       *         - Сохраняем результат.
       *         - Двигаем left и right к центру, пока не встретим ДРУГИЕ числа
       *           (чтобы не получить ту же комбинацию снова).
       *
       *      3. ЕСЛИ сумма < target:
       *         - Нам не хватает веса. Сдвигаем 'left' ВПРАВО (к бóльшим числам).
       *
       *      4. ЕСЛИ сумма > target:
       *         - У нас перебор. Сдвигаем 'right' ВЛЕВО (к меньшим числам).
       */
      let left = j + 1;
      let right = nums.length - 1;
      while (left < right) {
        const sum = nums[i] + nums[j] + nums[left] + nums[right];
        if (sum === target) {
          result.push([nums[i], nums[j], nums[left], nums[right]]);
          left++;
          right--;
          while (nums[right] === nums[right + 1] && left < right) {
            right--;
          }
          while (nums[left] === nums[left - 1] && left < right) {
            left++;
          }
        } else if (sum < target) {
          left++;
        } else if (sum > target) {
          right--;
        }
      }
    }
  }
  /**
   * 5. ЗАВЕРШЕНИЕ:
   *    - Возвращаем массив всех найденных уникальных комбинаций.
   */

  return result;
}
