/**
 * Задача №16: 3Sum Closest (Medium)
 *
 * Описание:
 * Дан массив целых чисел `nums` и число `target`. Нужно найти три числа,
 * сумма которых максимально близка к `target`, и вернуть эту сумму.
 *
 * Алгоритм:
 * 1. Сортировка: Сначала сортируем массив O(n log n). Это позволяет использовать
 *    метод двух указателей.
 * 2. Итерация: Проходим циклом по массиву, фиксируя первый элемент `nums[i]`.
 * 3. Два указателя: Для оставшейся части массива ставим указатели `left` (начало)
 *    и `right` (конец).
 * 4. Сравнение:
 *    - Если текущая сумма ближе к target, чем предыдущая найденная — обновляем результат.
 *    - Если сумма меньше target — двигаем `left` вправо (увеличиваем сумму).
 *    - Если сумма больше target — двигаем `right` влево (уменьшаем сумму).
 *
 * Сложность:
 * - Time: O(n²) — внешний цикл и два указателя внутри.
 * - Space: O(1) — (или O(log n) в зависимости от реализации sort).
 */

export const threeSumClosest = (num: number[], target: number): number => {
  /**
   * ПОШАГОВЫЙ АЛГОРИТМ (ДЛЯ НОВИЧКОВ):
   *
   * 1. СТРАТЕГИЯ "УПОРЯДОЧИВАНИЕ":
   *    Первым делом мы сортируем все числа от меньшего к большему.
   *    Это как расставить учеников в шеренгу по росту — так гораздо проще
   *    понимать, в какую сторону двигаться, чтобы изменить сумму.
   **/

  num.sort((a, b) => a - b);
  /** 2. ФИКСАЦИЯ ПЕРВОГО ИГРОКА (ЯКОРЬ):
   *    Мы начинаем проходить по очереди каждое число в массиве.
   *    Мы говорим: "Пусть это число точно будет в нашей тройке".
   *    Теперь нам осталось найти всего два других числа.
   **/
  let currentTarget = num[0] + num[1] + num[2];

  for (let i = 0; i < num.length; i++) {
    /** 3. МЕТОД "СУЖАЮЩИХСЯ ТИСКОВ":
     *    Для поиска остальных двух чисел мы не перебираем всё подряд.
     *    Мы ставим два указателя:
     *    - Один (ЛЕВЫЙ) в самое начало оставшегося ряда (на самое маленькое число).
     *    - Второй (ПРАВЫЙ) в самый конец ряда (на самое большое число).
     */
    let current = i;
    let left = i + 1;
    let right = num.length - 1;

    while (left < right) {
      /** 4. ОЦЕНКА И КОРРЕКТИРОВКА:
       *    Считаем сумму этих трёх чисел и сравниваем с нашей целью (target):
       *    - Если сумма СЛИШКОМ МАЛА: Сдвигаем ЛЕВЫЙ указатель вправо (берем число покрупнее).
       *    - Если сумма СЛИШКОМ ВЕЛИКА: Сдвигаем ПРАВЫЙ указатель влево (берем число поменьше).
       *    - Если сумма СОВПАЛА с целью: Ура, это идеальный ответ, сразу выходим!
       */

      const sum = num[current] + num[right] + num[left];

      if (sum === target) {
        return target;
      }

      const currentDistance = Math.abs(sum - target);
      const bestDistance = Math.abs(currentTarget - target);

      if (currentDistance < bestDistance) {
        currentTarget = sum;
      }

      if (sum > target) {
        right--;
      } else if (sum < target) {
        left++;
      }
    }
  }

  /** 6. ИТОГ:
   *    Когда мы проверили все возможные комбинации таким умным способом,
   *    мы возвращаем тот самый "рекорд", который оказался ближе всего к цели.
   */
  return currentTarget;
};
