/**
 * PROBLEM: 15. 3Sum (Medium)
 *
 * Найти все уникальные тройки чисел в массиве, сумма которых равна 0.
 *
 * ОСНОВНЫЕ ВЫЗОВЫ:
 * 1. Исключить дубликаты (одинаковые тройки значений в разном порядке).
 * 2. Оптимизировать время: O(n^2) вместо "наивного" O(n^3).
 * 3. Обработка краевых случаев: массивы длиной < 3.
 *
 * АЛГОРИТМ (Two Pointers + Sorting):
 *
 * 1. СОРТИРОВКА: Сначала сортируем nums. Это позволяет нам:
 *    - Группировать дубликаты вместе для легкого пропуска.
 *    - Использовать два указателя (двигаясь навстречу, мы понимаем,
 *      нужно ли нам увеличить или уменьшить сумму).
 *
 * 2. ФИКСАЦИЯ ПЕРВОГО ЧИСЛА (i): Проходим циклом по массиву.
 *    - Если текущее число совпадает с предыдущим (nums[i] == nums[i-1]),
 *      пропускаем итерацию, так как все тройки для него уже найдены.
 *
 * 3. ДВА УКАЗАТЕЛЯ (left, right): Для каждого nums[i] ищем два других числа
 *    в остатке массива (справа от i).
 *    - Если (nums[i] + nums[left] + nums[right] == 0) — БИНГО! Сохраняем.
 *    - Если сумма < 0: Нам нужно число побольше, двигаем left вправо.
 *    - Если сумма > 0: Нам нужно число поменьше, двигаем right влево.
 *
 * 4. УМНОЕ СМЕЩЕНИЕ: После нахождения тройки, смещаем указатели left и right
 *    через все идущие подряд одинаковые числа, чтобы избежать дублей в ответе.
 */

export function threeSum(nums: number[]): number[][] {
  /**
   * ПОШАГОВАЯ РЕАЛИЗАЦИЯ 3SUM:
   *
   * ШАГ 1: Инициализация
   * - Создай пустой массив 'result', куда будешь складывать найденные тройки.
   * - Отсортируй входящий массив 'nums' (важно: используй nums.sort((a, b) => a - b)).
   */
  const result = [];
  nums.sort((a, b) => a - b);

  /**
   * ШАГ 2: Внешний цикл (Фиксация первого элемента)
   * - Начни цикл 'for' от i = 0 до конца массива.
   * - Проверка на дубликат: если nums[i] совпадает с nums[i-1], используй 'continue',
   *   чтобы не обрабатывать одно и то же стартовое число дважды.
   */

  for (let i = 0; i < nums.length; i++) {
    if (i > 0 && nums[i] === nums[i - 1]) {
      continue;
    }

    /**
     * ШАГ 3: Настройка двух указателей
     * - Внутри цикла 'for' создай две переменные:
     *   'left' (равна i + 1) и 'right' (индекс последнего элемента массива).
     */

    let left = i + 1;
    let right = nums.length - 1;

    /**
     * ШАГ 4: Внутренний поиск (Цикл while)
     * - Пока 'left' меньше 'right':
     *   1. Вычисли сумму: nums[i] + nums[left] + nums[right].
     *   2. Если сумма РАВНА 0:
     *      - Добавь [nums[i], nums[left], nums[right]] в 'result'.
     *      - Сдвигай 'left' вправо, а 'right' влево, пока они не упрутся в другие числа (пропуск дублей).
     *      - Сдвинь их еще на один шаг, чтобы перейти к новым значениям.
     *   3. Если сумма МЕНЬШЕ 0:
     *      - Нам нужно число побольше, поэтому сделай 'left++'.
     *   4. Если сумма БОЛЬШЕ 0:
     *      - Нам нужно число поменьше, поэтому сделай 'right--'.
     */

    while (left < right) {
      const sum = nums[i] + nums[left] + nums[right];
      if (sum === 0) {
        result.push([nums[i], nums[left], nums[right]]);

        while (left < right && nums[left] === nums[left + 1]) {
          left++;
        }
        while (left < right && nums[right] === nums[right - 1]) {
          right--;
        }
        left++;
        right--;
      } else if (sum < 0) {
        left++;
      } else if (sum > 0) {
        right--;
      }
    }
  }

  /**
   * ШАГ 5: Финал
   * - Верни заполненный массив 'result'.
   */

  return result;
}
