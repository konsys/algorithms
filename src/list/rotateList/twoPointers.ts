/*

Метод «Двух указателей» (Two Pointers) или методом «Окна».
Вместо того чтобы сначала зацикливать список, а потом его разрывать, мы используем два указателя: fast (быстрый) и slow (медленный).
Суть метода (без зацикливания):
1. Сдвигаем fast вперед на k шагов. Теперь между head и fast образовался «разрыв» в k узлов.
2. Двигаем оба указателя (fast и slow), пока fast не дойдет до последнего узла.
3. Магия: Из-за того, что fast шел впереди на k шагов, когда он доберется до конца,
slow будет стоять как раз перед тем элементом, который должен стать новой головой.
Перебрасываем связи:
fast.next = head (хвост цепляется к началу).
newHead = slow.next (находим новое начало).

 */

/**
 * АЛГОРИТМ "ДИСТАНЦИОННОЕ ОКНО" (Two Pointers Method):
 *
 * 1. ПРЕДВАРИТЕЛЬНЫЙ РАСЧЕТ:
 *    - Проходим список один раз, чтобы узнать его длину (n).
 *    - Вычисляем эффективное k = k % n. Если k = 0, возвращаем head.
 *
 * 2. СОЗДАНИЕ ДИСТАНЦИИ:
 *    - Устанавливаем два указателя (fast и slow) на начало (head).
 *    - Сдвигаем только fast вперед на k шагов.
 *    - Теперь между ними "окно" шириной k узлов.
 *
 * 3. СИНХРОННОЕ ДВИЖЕНИЕ:
 *    - Двигаем оба указателя одновременно (fast = fast.next, slow = slow.next).
 *    - Останавливаемся, когда fast достигнет последнего узла (fast.next === null).
 *    - В этот момент slow будет стоять ровно ПЕРЕД узлом, который должен стать новым началом.
 *
 * 4. ПЕРЕБОРОВКА СВЯЗЕЙ:
 *    - Назначаем новую голову: newHead = slow.next.
 *    - Соединяем старый хвост с началом: fast.next = head.
 *    - Разрываем связь у нового хвоста: slow.next = null.
 *
 * 5. РЕЗУЛЬТАТ:
 *    - Возвращаем newHead.
 *
 * СЛОЖНОСТЬ: Time O(N), Space O(1).
 */
/**
 * ПРИМЕР РАБОТЫ МЕТОДА ДВУХ УКАЗАТЕЛЕЙ:
 * Вход: head = [1, 2, 3, 4, 5], k = 2
 *
 * 1. ПОДГОТОВКА:
 *    Вычисляем длину (n=5). k = 2 % 5 = 2.
 *    fast -> [1], slow -> [1]
 *
 * 2. СОЗДАЕМ ДИСТАНЦИЮ (fast идет на k=2 шага вперед):
 *    Шаг 1: fast -> [2]
 *    Шаг 2: fast -> [3]
 *    Результат: slow стоит на [1], fast стоит на [3]. Между ними "окно" в 2 узла.
 *
 * 3. СИНХРОННЫЙ ДВИЖЕК (пока fast.next != null):
 *    - fast -> [4], slow -> [2]
 *    - fast -> [5], slow -> [3] (СТОП, fast.next — это null)
 *
 * 4. ПЕРЕБРОС УКАЗАТЕЛЕЙ:
 *    - newHead = slow.next          // Новое начало — это [4]
 *    - fast.next = head             // Соединяем хвост [5] с началом [1]
 *    - slow.next = null             // Разрезаем связь после [3]
 *
 * Итог: [4] -> [5] -> [1] -> [2] -> [3] -> null
 */
