/*
Задача «Rotate List» — это классика работы со связанными списками. Основная хитрость здесь в том,
что если k больше длины списка, нам не нужно делать лишние круги — достаточно использовать остаток от деления.

Алгоритм решения
Чтобы эффективно повернуть список, мы выполним следующие шаги:
1. Находим длину списка (n): Заодно доходим до последнего узла.
2. Зацикливаем список: Соединяем последний узел с головой (tail.next = head), превращая список в кольцо.
3. Оптимизируем k: Новое значение k = k(mod n). Если k = 0, возвращаем исходную голову.
4. Находим точку разрыва: Нам нужно пройти n - k шагов от старой головы, чтобы найти новый «хвост».
5. Разрываем кольцо: Узел, следующий за новым хвостом, станет новой головой, а tail.next устанавливаем в null.

 */

/**
 * АЛГОРИТМ "ЦИКЛИЧЕСКИЙ СДВИГ" (Ring Method):
 *
 * 1. ПРОВЕРКА ГРАНИЦ:
 *    Если head пустой или k = 0, возвращаем head как есть.
 *
 * 2. ИЗМЕРЕНИЕ И ЗАЦИКЛИВАНИЕ:
 *    - Проходим до последнего узла (tail), считая длину списка (n).
 *    - Соединяем tail.next = head. Теперь список стал бесконечным кольцом.
 *
 * 3. ВЫЧИСЛЕНИЕ СМЕЩЕНИЯ:
 *    - Чтобы не делать лишних кругов, берем k = k % n.
 *    - Если новый k = 0, разрываем кольцо в том же месте и возвращаем head.
 *
 * 4. ПОИСК ТОЧКИ РАЗРЫВА:
 *    - Нам нужно оставить (n - k) элементов в начале.
 *    - Отсчитываем (n - k - 1) шагов от текущей головы, чтобы найти "новый хвост".
 *
 * 5. ФОРМИРОВАНИЕ НОВОГО СПИСКА:
 *    - Узел, следующий за "новым хвостом", объявляем "новой головой" (newHead).
 *    - У "нового хвоста" зануляем ссылку next (next = null), чтобы разомкнуть кольцо.
 *
 * 6. РЕЗУЛЬТАТ:
 *    Возвращаем newHead.
 *
 * СЛОЖНОСТЬ: Time O(N), Space O(1).
 */

/**
 * Определение узла односвязного списка
 */
class ListNode {
  val: number;
  next: ListNode | null;

  constructor(val?: number, next?: ListNode | null) {
    this.val = val === undefined ? 0 : val;
    this.next = next === undefined ? null : next;
  }
}

export function rotateRight(head: ListNode | null, k: number): ListNode | null {
  // 1. Простейшие случаи: если список пуст, в нем 1 элемент или k = 0
  if (!head || !head.next || k === 0) {
    return head;
  }

  // 2. Считаем длину списка и находим текущий хвост
  let lastNode: ListNode = head;
  let length: number = 1;

  while (lastNode.next) {
    lastNode = lastNode.next;
    length++;
  }

  // 3. Оптимизируем k: убираем лишние полные круги
  k = k % length;
  if (k === 0) return head;

  // 4. Зацикливаем список: соединяем хвост с головой
  lastNode.next = head;

  // 5. Ищем новый хвост: он находится на расстоянии (length - k) от начала
  let stepsToNewTail: number = length - k;
  let newTail: ListNode = head;

  // Идем до узла, который станет последним
  for (let i = 0; i < stepsToNewTail - 1; i++) {
    newTail = newTail.next!;
  }

  // 6. Устанавливаем новую голову и разрываем кольцо
  const newHead: ListNode | null = newTail.next;
  newTail.next = null;

  return newHead;
}
