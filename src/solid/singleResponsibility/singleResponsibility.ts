// Принцип единственной ответственности — у класса должна быть только одна причина для изменения (одна задача).
/**
 * В React принцип единственной ответственности (Single Responsibility Principle) означает, что один компонент (или функция/хук) должен решать ровно одну задачу.
 * Если компонент становится слишком большим, его трудно тестировать, переиспользовать и понимать.
 * Как это выглядит на практике:
 * 1. Декомпозиция UI (Отображение)
 * Вместо одного огромного компонента UserProfile, который рисует и аватар, и список постов, и настройки, лучше разделить его на мелкие части:
 * UserAvatar — только отображает фото.
 * UserStats — только показывает количество подписчиков.
 * PostList — только выводит список постов.
 * 2. Разделение логики и представления
 * Компонент не должен одновременно «ходить в базу за данными» и «красиво их рисовать».
 * Плохо: Компонент делает fetch внутри useEffect и тут же верстает сложную таблицу.
 * Хорошо: Вынести логику получения данных в кастомный хук (например, useFetchUsers), а компонент оставить «глупым» — он просто получает массив данных через пропсы и выводит их.
 * 3. Управление состоянием
 * Если форма содержит 20 полей и сложную валидацию, логику валидации лучше вынести в отдельный служебный файл или хук, чтобы компонент формы отвечал только за связку полей с UI.
 * Главные преимущества:
 * Легкое тестирование: Проще написать тест для маленькой функции, чем для компонента на 500 строк.
 * Переиспользование: Мелкий компонент Button можно использовать везде, а «Кнопку, которая еще и отправляет запрос на конкретный API» — только в одном месте.
 * Читаемость: Сразу понятно, за что отвечает файл, просто взглянув на его название.
 */
