/**
 * Буква «O» — Open/Closed Principle (Принцип открытости/закрытости)
 * Суть: Программные сущности (компоненты, хуки) должны быть открыты для расширения, но закрыты для модификации.

 * В React это означает, что вы должны иметь возможность добавить новую функциональность или изменить поведение компонента, не меняя его исходный код.

 * Как это реализуется в React:
 * Использование пропсов и композиции: Вместо того чтобы прописывать жесткую логику внутри компонента
 * (например, через if/else или switch для разных типов кнопок), компонент проектируется так, чтобы принимать стили или дополнительные элементы через пропсы.
 * Свойство children: Позволяет передавать в компонент любой контент, расширяя его визуальную часть без правки самого компонента.
 * HOC (Компоненты высшего порядка) и кастомные хуки: Позволяют «оборачивать» существующую логику или добавлять новую, не затрагивая основной код компонента.
 * Паттерн Render Props: Передача функции рендеринга как пропса для изменения отображения данных без изменения логики их обработки.

 * Пример нарушения и исправления:
 * ❌ Плохо: Компонент Alert, внутри которого прописаны условия: if (type === 'success') return .... Чтобы добавить новый тип уведомления, вам придется лезть в код Alert и добавлять новую ветку условия.
 * ✅ Хорошо: Базовый компонент Alert, который принимает className и children. Вы создаете SuccessAlert, который просто использует Alert с нужными параметрами.
 * Базовый компонент «закрыт» для правок, но «открыт» для создания новых типов уведомлений на его основе.

 */


interface ListProps<T> {
  items: T[];
  // Функция рендеринга позволяет расширять отображение извне
  renderItem: (item: T) => React.ReactNode;
  keyExtractor: (item: T) => string | number;
}

// Универсальный компонент (Закрыт для модификации)
function GenericList<T>({ items, renderItem, keyExtractor }: ListProps<T>) {
  return (
    <div className="list-container">
      {items.map((item) => (
          <div key={keyExtractor(item)} className="list-item">
        {renderItem(item)}
        </div>
  ))}
  </div>
);
}

// Использование (Расширение через аргументы)
interface User { id: number; name: string }

const App = () => {
  const users: User[] = [{ id: 1, name: "Ivan" }];

  return (
    <GenericList
      items={users}
  keyExtractor={(user) => user.id}
  renderItem={(user) => <strong>Пользователь: {user.name}</strong>}
  />
);
};